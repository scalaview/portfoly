import numpy as np

a = np.array([0, 0.5, 1, 1.5, 2.5])
type(a)
# <class 'numpy.ndarray'>

a.cumsum()
# array([ 0. ,  0.5,  1.5,  3. ,  5.5])

a.std()
# 0.86023252670426265

np.sqrt(a)
# array([ 0.        ,  0.70710678,  1.        ,  1.22474487,  1.58113883])

b = np.array([a, a*2])
# array([[ 0. ,  0.5,  1. ,  1.5,  2.5],
       # [ 0. ,  1. ,  2. ,  3. ,  5. ]])

b.sum(axis=0)
# 列总和
# array([ 0. ,  1.5,  3. ,  4.5,  7.5])

b.sum(axis=1)
# 行总和
# array([  5.5,  11. ])

